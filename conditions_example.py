
# Ask the user to specify his / her age.
age = int(input("Please, enter your age: "))

# Check the user input and print certain output.

# If condition example...
# Evaluate the primary condition.

if age < 21:
    print("You are still underage! You cannot consume alcohol in the USA!")

# If the condition above is not matched, then do something else.
elif age >= 21:
    print("You are an adult. You can consume alcohol in the USA!")

# Else condition example...
else: 
    print("You have not specified your age!")
     
